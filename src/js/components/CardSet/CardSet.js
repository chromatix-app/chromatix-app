// ======================================================================
// IMPORTS
// ======================================================================

import { Card } from 'js/components';
import style from './CardSet.module.scss';

// ======================================================================
// COMPONENT
// ======================================================================

const CardSet = ({ entries }) => {
  if (entries) {
    return (
      <div className={style.wrap}>
        {entries.map((entry, index) => (
          <Card key={index} {...entry} />
        ))}
      </div>
    );
  }
};

// ======================================================================
// EXPORT
// ======================================================================

export default CardSet;
