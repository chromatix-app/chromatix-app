// ======================================================================
// IMPORTS
// ======================================================================

import { useDispatch } from 'react-redux';

// ======================================================================
// COMPONENT
// ======================================================================

const Login = () => {
  const dispatch = useDispatch();

  return (
    <div>
      <button
        onClick={() => {
          dispatch.appModel.login();
        }}
      >
        Login
      </button>
    </div>
  );
};

// ======================================================================
// EXPORT
// ======================================================================

export default Login;
